#!/bin/bash
# =============================================================================
# SparkFox v1.1.0 合规审计报告生成脚本（Sub-Step 12.6.2）
# -----------------------------------------------------------------------------
# 用途：验证 docs/SparkFox-v1.1.0-合规审计报告.md 与配套数据文件完整性
# 方法学：依赖扫描 + 许可证分类 + 致谢矩阵 + 风险评估 + AGPL 合规 8 项检查
# 兼容性：bash 脚本，GitHub Actions / Linux / macOS / Git Bash on Windows 均可运行
# =============================================================================
set -e

# 工作目录定位（脚本可从任意目录调用）
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ROOT_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"

# 待校验文件路径（相对仓库根）
REPORT="$ROOT_DIR/docs/SparkFox-v1.1.0-合规审计报告.md"
INVENTORY="$ROOT_DIR/docs/compliance/license_inventory.json"
MATRIX="$ROOT_DIR/docs/compliance/attribution_matrix.csv"

echo "=== 生成 SparkFox v1.1.0 合规审计报告 ==="
echo "报告路径: $REPORT"
echo "许可证清单: $INVENTORY"
echo "致谢矩阵:  $MATRIX"
echo

# -----------------------------------------------------------------------------
# 检查 1: 报告含执行摘要（Executive Summary / 执行摘要）
# -----------------------------------------------------------------------------
if grep -q "执行摘要\|Executive Summary" "$REPORT"; then
  echo "✓ 检查 1 通过: 报告含执行摘要"
else
  echo "✗ 检查 1 失败: 报告缺少执行摘要"
  exit 1
fi

# -----------------------------------------------------------------------------
# 检查 2: 报告含依赖许可证清单（引用 license_inventory.json）
# -----------------------------------------------------------------------------
if grep -q "license_inventory\|许可证清单" "$REPORT" && [ -f "$INVENTORY" ]; then
  echo "✓ 检查 2 通过: 报告含许可证清单"
else
  echo "✗ 检查 2 失败: 报告缺少许可证清单"
  exit 1
fi

# -----------------------------------------------------------------------------
# 检查 3: 报告含致谢矩阵（引用 attribution_matrix.csv）
# -----------------------------------------------------------------------------
if grep -q "attribution_matrix\|致谢矩阵" "$REPORT" && [ -f "$MATRIX" ]; then
  echo "✓ 检查 3 通过: 报告含致谢矩阵"
else
  echo "✗ 检查 3 失败: 报告缺少致谢矩阵"
  exit 1
fi

# -----------------------------------------------------------------------------
# 检查 4: 报告含 AGPL 合规验证（≥ 8 项检查项）
# 关键字：AGPL 合规 / 合规验证 / 检查项（每出现一次计一项）
# -----------------------------------------------------------------------------
agpl_count=$(grep -c "AGPL.*合规\|合规.*验证\|检查项" "$REPORT" || true)
if [ "$agpl_count" -ge 8 ]; then
  echo "✓ 检查 4 通过: 报告含 AGPL 合规验证（$agpl_count 项）"
else
  echo "✗ 检查 4 失败: 报告 AGPL 合规验证项不足（$agpl_count/8）"
  exit 1
fi

# -----------------------------------------------------------------------------
# 检查 5: 报告含风险评估（高/中/低风险 + 缓解措施）
# -----------------------------------------------------------------------------
if grep -q "高风险\|中风险\|低风险" "$REPORT" && grep -q "缓解措施\|Mitigation" "$REPORT"; then
  echo "✓ 检查 5 通过: 报告含风险评估"
else
  echo "✗ 检查 5 失败: 报告缺少风险评估"
  exit 1
fi

# -----------------------------------------------------------------------------
# 检查 6: 报告含结论（通过 / 有条件通过 / 不通过 / Conclusion）
# -----------------------------------------------------------------------------
if grep -q "通过\|有条件通过\|不通过\|Conclusion" "$REPORT"; then
  echo "✓ 检查 6 通过: 报告含结论"
else
  echo "✗ 检查 6 失败: 报告缺少结论"
  exit 1
fi

echo
echo "=== 所有检查通过 ==="
